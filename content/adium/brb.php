<h3>How to edit the reasons &amp; add your own</h3>

<p>Firstly I'd go make sure you have the latest update as I rewrote the script to make it more user 
readable.
</p>

<p>Basically the scripts for adium are bundles.  You need to dig down into <code>/Users/&laquo;your user 
name&raquo;/Library/Application\ Support/Adium\ 2.0/Scripts/</code> then right click 
<code>brb.AdiumScripts</code> and select <code>Show Package Contents</code>.  Open 
<code>Contents/Resources/brb.scpt</code> in Script Editor.
</p>

<p>All the quotes are in the main array (list), { "Quote 1", "Quote 2"}.  The ¬ signifies a continuation 
of a line on the next line.</p>

<p>When you're done, save the file, and relaunch adium to make it recognise the new changes.</p>

<p>Hope that helps. :)</p>

<p><em>(To right click with a one button mouse, hold down control (&#x2303;) and then click)</em>
</p>

