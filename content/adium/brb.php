<h3>Description</h3>

<p>Upon the script command <b>%_brb</b> <em>(See footnote)</em> the contact is sent a brb - xxxxx message. 
xxxx is replaced by a totally unbelievable reason for going away.  Just a little joke script I made to 
spice up brb messages...</p>

<p>I'm always on the lookout for new brb reasons.  Anyone that cares to submit more is very welcome to. 
Email them to me -- <em>dev [at] caius [dot] name</em>
</p>

<p>If you wish to edit the script once you've installed it, that is possible, please see the end of this 
page for the how-to.
